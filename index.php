        <h2>Visualisation</h2>
        <p>This tool allows you to specify:</p>
        <ul>
            <li>the tower height. Some proposed installations quote a tower height and then a "rotor" diameter.
                For example, a quoted tower of 119m with a "rotor" diameter of 162m, would actually mean the tower
                is perhaps 118m high, the nacelle on top is another 2m (so the axle centre height is 119m -
                the middle of the nacelle height) and the blade length from the centre of the axle is (162 / 2),
                that is 81m. That would give top of tip height as 200m (119 + 81).</li>
            <li>the taper of the tower. This specified as the diameter at the base and the diameter at the top (if they are different, mathematically, this is a
                <a href="https://en.wikipedia.org/wiki/Frustum" title="Link to Wikipedia page explaining frusta">frustum</a>
                - not a word we come across very often)</li>
            <li>the shape of the nacelle (cylindrical or rectangular) and dimensions</li>
            <li>the number of blades</li>
            <li>the blade length (so half the "rotor" diameter in the above example)
                - this must be at least 5m less than the tower height (towers often have a service access door near the base,
                so there needs to be clearaance for this and the ground)</li>
            <li>the blade root diameter (where it joins to the axle)</li>
            <li>the position of the "first" blade, so that, for example, with a 3 blader, the first blade is shown
                pointing up (12 o'clock - giving an inverted Y shape for a 3 blader), down (6 o'clock -
                giving a Y shape for the 3 blader) or horizontal (3 o'clock)</li>
            <li>the orientiation of the blade axis for the first set of visualisations. In the UK, the predominant
                wind is from the South West (say 225 degrees from North), but at the location of interest, it may be
                different - it's in this direction that the turbines are most likely to be pointing.</li>
            <li>the number of wind directions to include as options. So, with 4 directions, that would be, say,
                South, East, North and West (if the first direction were S - for SW, that would be SW, NW, NE, SE).
                This allows for viewing the turbines close enough full "face on" from anywhere.
                When the visualisation is first opened, the first wind direction will be displayed. Depending on the
                chosen visualisation tool, you can "turn off" that first wind direction visualisation
                and turn on one of the others. The tool might allow you to turn on more than one wind direction at once,
                but that would be pointless and probably unhelpful.
                </li>
            <li>the locations(s) of the turbines, which are taken to be the centre of the base of the towers. These can be entered as:
                <ul>
                    <li><a href="https://en.wikipedia.org/wiki/World_Geodetic_System#WGS_84"
                           title="Link to within Wikipedia page explaining World Geodetic Systems">WGS84</a>
                        latitude and longitude in decimal degrees,
                        e.g. <code>52.123456 -3.123456</code>
                    </li>
                    <li><a href="https://en.wikipedia.org/wiki/World_Geodetic_System#WGS_84"
                           title="Link to within Wikipedia page explaining World Geodetic Systems">WGS84</a>
                        latitude and longitude in degrees, minutes and seconds (DMS),
                        e.g. <code>52&deg;12'34"N 3&deg;12'34"W</code> or <code>52:12:34N 3:12:34W</code>
                    </li>
                    <li><a href="https://www.ordnancesurvey.co.uk/" title="Link to the Ordnance Survey's website">UK Ordance Survey</a> -
                        <a href="https://en.wikipedia.org/wiki/Ordnance_Survey_National_Grid#Grid_letters"
                           title="Link to within the Wikipedia page explaing OS grid refrences">grid with letters</a>,
                        e.g. <code>TQ1234512345</code>
                        - So, UK, only
                    </li>
                    <li><a href="https://www.ordnancesurvey.co.uk/" title="Link to the Ordnance Survey's website">UK Ordance Survey</a> -
                        <a href="https://en.wikipedia.org/wiki/Ordnance_Survey_National_Grid#All-numeric_grid_references"
                           title="Link to within the Wikipedia page explaing OS grid refrences">grid with eastings and northings</a>,
                        e.g. <code>123456 123456</code>
                        - So, UK, only
                    </li>
                </ul>
            </li>
            <li>If you don't know the locations of each individual turbine, you can "cheat" by entering a single location followed by
                a * and the count of turbines at that location. Any of the accepted formats above can be used.
                <br />
                For example, <code>52.123456 -3.123456 * 10</code>
                <br />
                That will generate 10 turbines centred on that location in a hexagonal packing arrangment with spacing
                between them to ensure the blades don't clash. If you need larger spacing, than add more *s
                For example, <code>52.123456 -3.123456 *** 10</code>
                <br />
                The temptation is to go a little too far, in which case, you'll find that the visualisation tool (e.g. Google Earth)
                will struggle and may run of memory on your system (or simply be very slow due to
                <a href="https://en.wikipedia.org/wiki/Memory_paging" title="Link to Wikipedia memory paging article">paging</a>).

            </li>
        </ul>
        <p>All units are <a href="https://en.wikipedia.org/wiki/International_System_of_Units"
                            title="Link to Wikipedia page covering the International System of Units">SI</a>,
            so metres, just numbers (e.g. blades), and degrees of the compass. For locations, see above.</p>
