                    <ul>
                        <li>the Tower. This is almost always a hollow cylinder, frequently tapering towards the top for the larger turbines
                            (say, more then 100m tall)
                        </li>
                        <li>the Nacelle. This sits on top of the tower with an axle protruding at the front to which the blades are attached.
                            This varies from
                            <a href="https://en.wikipedia.org/wiki/List_of_wind_turbine_manufacturers"
                               title="Link to Wikipedia page with a list of wind turbine manufacturers">manufacturer</a>
                            to manufacturer and with turbine size,
                            but usually contains the electricity generating equipment and mechanisms for aligning the turbine
                            with the current wind direction.</li>
                        <li>the Blades. Virtually all modern day, industrial scale wind turbines have three blades.
                            This is for a combination of economic and efficiency reasons,
                            and there were anecdotal reports in early 2 blade installations of leading to more reports of
                            apparent faults by lay observers versus 3 or more bladed designs because of the difference seen
                            between when the 2 blades were horizontal to when they were vertical
                            (very thin to two full blade lengths as seen from the side) being unsettling to the observer.
                            <a href="https://www.electricaltechnology.org/2024/07/wind-turbines-3-blades-instead-4-5.html"
                               title="Link to Electrical Technology Website">This page</a>
                            has a technical explanation for why 3 blades are the optimum.
                        </li>
